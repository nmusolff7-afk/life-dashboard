// Translation function — returns translated string or fallback to English or key
function t(key) {
  const lang = localStorage.getItem("appLang") || "en";
  return (I18N[lang] && I18N[lang][key]) || (I18N.en && I18N.en[key]) || key;
}

// Apply translations to all elements with data-i18n attribute
function applyLang() {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.dataset.i18n;
    const val = t(key);
    if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
      el.placeholder = val;
    } else {
      el.textContent = val;
    }
  });
  // Update nav tabs
  document.querySelectorAll(".nav-btn").forEach(btn => {
    const tab = btn.dataset.tab;
    const map = { home: "home", meals: "nutrition", workout: "fitness", progress: "progress", mind: "status", profile: "profile" };
    if (map[tab]) btn.lastChild.textContent = t(map[tab]);
  });
}
